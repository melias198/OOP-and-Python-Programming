class Calculator:
    def add(self,a,b):
        print(a+b)
    def deduct(self,a,b):
        print(a-b)
    def divide(self,a,b):
        print(a/b)
    def multiply(self,a,b):
        print(a*b)

calculator = Calculator()

calculator.add(2,7)
calculator.deduct(10,4)
calculator.multiply(3,5)
calculator.divide(20,4)